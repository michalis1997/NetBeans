    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed

        String cust_name = name.getText();
        String cust_email = email.getText();
        String cust_password = String.valueOf(password.getPassword());      
        String cust_member = member.getSelectedItem().toString();
        String Phonenumber = phonenumber.getText();
        String address = String.valueOf(Address.getText());
        
        String customer_email= null;

        PreparedStatement st, ps;
        String query = "INSERT INTO customers (customerId, username, Member, email, password,phonenumber,Address) VALUES(?, ?, ?, ?,? ,?,?)";
        String query1 = "SELECT email FROM customers WHERE email=?";
        String query2 = "INSERT INTO administator(Id, email, username,password,phonenumber,Address Member) VALUES(?, ?, ?, ?,?,?,? )";
        try {
            ps = MyConnection.getConnection().prepareStatement(query1);
            ps.setString(1, cust_name);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                customer_email = rs.getString("email");
            }
            if(cust_member.equals("Business")){
                st = MyConnection.getConnection().prepareStatement(query2);
                st.setString(1, null);
                st.setString(2, cust_name);
                st.setString(3, cust_member);
                st.setString(4, cust_email);
                st.setString(5, cust_password);
                st.setString(6,Phonenumber);
                st.setString(7,address);
            }
            else if (cust_email.equals(customer_email)){
                JOptionPane.showMessageDialog(null,"Email already exists");
            } else {
                st = MyConnection.getConnection().prepareStatement(query);
                
                st.setString(1, null);
                st.setString(2, cust_name);
                st.setString(3, cust_member);
                st.setString(4, cust_email);
                st.setString(5, cust_password);
                st.setString(6,Phonenumber);
                st.setString(7,address);

                if(st.executeUpdate() > 0)
                {
                    JOptionPane.showMessageDialog(null,"New Customer Added Successfully");
                }
            }
        }   catch (SQLException ex) {
                Logger.getLogger(sign_up.class.getName()).log(Level.SEVERE, null, ex);
         }   

    }//GEN-LAST:event_registerActionPerformed

